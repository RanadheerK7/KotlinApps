fun main() {
    val num1 = 20
    val num2 = 4

    val add = num1 + num2
    println("Addition: $add")

    val sub = num1 - num2
    println("Subtraction: $sub")

    val mul = num1 * num2
    println("Multiplication: $mul")

    val div = num1 / num2
    println("Division: $div")

    val mod = num1 % num2
    println("Modulus: $mod")
}